## Linear Equations

Each variable such as ``x^n``, ``y^n``, or ``z^n``

- ✅ ``2x + 1 = 7``
- ❌ ``x^\textcolor{red}{2} = 2y``
- ❌ ``\textcolor{red}{\sin(x)} = 2x`` 
- ❌ ``\textcolor{red}{\sqrt{x}}=3x+1``

### Definition: Linear Equation
``a_1v_1+a_2v_2+a_3v_3+\cdots+a_nv_n=b``

Where
- ``\forall a\in\mathbb{R}`` (Coefficients are all Real)
- ``b\in\mathbb{R}`` (Constant is Real)

### Definition: System of Linear Equations
A collection of one or more linear equations. One linear equation only would be kinda silly, though it technically qualifies.

### Determination
A linear system is either *underdetermined*, *determined*, or *overdetermined*. Let's take a look at a few examples.
